package com.capgemini.employee.dto;

public class Employee {
	private int empId;
	private String empName;
	private double empSal;
	private String empDesign;
	private String insSch;
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public double getEmpSal() {
		return empSal;
	}
	public void setEmpSal(double empSal) {
		this.empSal = empSal;
	}
	public String getEmpDesign() {
		return empDesign;
	}
	public void setEmpDesign(String empDesign) {
		this.empDesign = empDesign;
	}
	public String getInsSch() {
		return insSch;
	}
	public void setInsSch(String insSch) {
		this.insSch = insSch;
	}

	
}
